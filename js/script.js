const messageTranslations = {
  es: "¡Empieza tu recorrido por la senda del Tolima!",
  en: "Start your journey along the Tolima path!"
};

const titleKeysByPage = {
  "index.html": "home.metaTitle",
  "": "home.metaTitle",
  "aboutUs.html": "about.metaTitle"
};

let currentLanguage = localStorage.getItem("siteLanguage") || "es";
let slideIndex = 1;

function getCurrentPage() {
  return window.location.pathname.split("/").pop() || "index.html";
}

function applyTranslations(language) {
  const dictionary = translations[language] || translations.es;
  const pageKey = titleKeysByPage[getCurrentPage()];

  if (pageKey && dictionary[pageKey]) {
    document.title = dictionary[pageKey];
  }

  document.documentElement.lang = language === "en" ? "en" : "es";

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (!key || dictionary[key] === undefined) return;
    const value = dictionary[key];

    if (element.tagName === "TITLE") {
      document.title = value;
    } else {
      element.textContent = value;
    }
  });
}

function mostrarMensaje() {
  mensajeDiv.textContent = messageTranslations[currentLanguage] || messageTranslations.es;
  mensajeDiv.style.display = "block";
  setTimeout(() => {
    mensajeDiv.style.display = "none";
  }, 3000);
}

function setLanguage(language, showMessage = false) {
  currentLanguage = language;
  localStorage.setItem("siteLanguage", language);
  applyTranslations(language);
  if (showMessage) {
    mostrarMensaje();
  }
}

function bindLanguageButtons() {
  const buttons = [
    { id: "bandera-es-mobile", language: "es" },
    { id: "bandera-en-mobile", language: "en" },
    { id: "bandera-es-desktop", language: "es" },
    { id: "bandera-en-desktop", language: "en" }
  ];

  buttons.forEach(({ id, language }) => {
    const button = document.getElementById(id);
    if (!button || button.dataset.languageBound === "true") return;

    button.dataset.languageBound = "true";
    button.addEventListener("click", function (event) {
      event.preventDefault();
      setLanguage(language, true);
    });
  });
}

function initializeNavbar() {
  fetch("navbar.html")
    .then((res) => res.text())
    .then((data) => {
      document.getElementById("navbar").innerHTML = data;

      const dropdowns = document.querySelectorAll(".dropdown");
      dropdowns.forEach((dropdown) => {
        const submenu = dropdown.querySelector(".submenu");
        if (submenu) {
          dropdown.addEventListener("mouseover", () => {
            submenu.style.display = "block";
          });

          dropdown.addEventListener("mouseout", () => {
            submenu.style.display = "none";
          });
        }
      });

      bindLanguageButtons();
      applyTranslations(currentLanguage);
    });
}

function plusSlides(n) {
  showSlides(slideIndex += n);
}

function currentSlide(n) {
  showSlides(slideIndex = n);
}

function showSlides(n) {
  const slides = document.getElementsByClassName("main_img");
  const dots = document.getElementsByClassName("dot");

  if (!slides.length || !dots.length) return;

  if (n > slides.length) { slideIndex = 1; }
  if (n < 1) { slideIndex = slides.length; }

  for (let i = 0; i < slides.length; i += 1) {
    slides[i].style.display = "none";
  }

  for (let i = 0; i < dots.length; i += 1) {
    dots[i].className = dots[i].className.replace(" active", "");
  }

  slides[slideIndex - 1].style.display = "block";
  dots[slideIndex - 1].className += " active";
}

function handleHashChange() {
  const hash = window.location.hash;
  const currentPage = getCurrentPage();

  if (currentPage === "index.html" || currentPage === "") {
    const misionSection = document.getElementById("mision-section");
    const visionSection = document.getElementById("vision-section");
    const slideshowContainer = document.querySelector(".slideshow-container");
    const dotsContainer = document.getElementById("dots-container");
    const container = document.querySelector(".container");
    const homeHeading = document.querySelector(".heading");

    if (hash === "#mision") {
      if (misionSection) misionSection.style.display = "block";
      if (visionSection) visionSection.style.display = "none";
      if (slideshowContainer) slideshowContainer.style.display = "none";
      if (dotsContainer) dotsContainer.style.display = "none";
      if (container) container.style.display = "none";
      if (homeHeading) homeHeading.style.display = "none";
    } else if (hash === "#vision") {
      if (misionSection) misionSection.style.display = "none";
      if (visionSection) visionSection.style.display = "block";
      if (slideshowContainer) slideshowContainer.style.display = "none";
      if (dotsContainer) dotsContainer.style.display = "none";
      if (container) container.style.display = "none";
      if (homeHeading) homeHeading.style.display = "none";
    } else {
      if (misionSection) misionSection.style.display = "none";
      if (visionSection) visionSection.style.display = "none";
      if (slideshowContainer) slideshowContainer.style.display = "block";
      if (dotsContainer) dotsContainer.style.display = "block";
      if (container) container.style.display = "block";
      if (homeHeading) homeHeading.style.display = "block";
    }
  }

  if (currentPage === "monumentos.html") {
    const sitiosHistoricos = document.getElementById("sitios-historicos");
    const arquitecturaModerna = document.getElementById("arquitectura-moderna");
    const headingSection = document.getElementById("heading-section");

    if (hash === "#sitios-historicos") {
      if (sitiosHistoricos) sitiosHistoricos.style.display = "block";
      if (arquitecturaModerna) arquitecturaModerna.style.display = "none";
      if (headingSection) headingSection.style.display = "none";
    } else if (hash === "#arquitectura-moderna") {
      if (sitiosHistoricos) sitiosHistoricos.style.display = "none";
      if (arquitecturaModerna) arquitecturaModerna.style.display = "block";
      if (headingSection) headingSection.style.display = "none";
    }
  }

  if (currentPage === "fiestas.html") {
    const concursosSection = document.getElementById("concursos");
    const sanPedroSection = document.getElementById("san-pedro");
    const headingFiestas = document.getElementById("heading-fiestas");

    if (hash === "#concursos") {
      if (concursosSection) concursosSection.style.display = "block";
      if (sanPedroSection) sanPedroSection.style.display = "none";
      if (headingFiestas) headingFiestas.style.display = "none";
    } else if (hash === "#san-pedro") {
      if (concursosSection) concursosSection.style.display = "none";
      if (sanPedroSection) sanPedroSection.style.display = "block";
      if (headingFiestas) headingFiestas.style.display = "none";
    }
  }

  if (currentPage === "musica.html") {
    const historiaSection = document.getElementById("musica-clasica");
    const ciudadMusicalSection = document.getElementById("musica-folclorica");
    const headingMusica = document.getElementById("heading-musica");

    if (hash === "#musica-clasica") {
      if (historiaSection) historiaSection.style.display = "block";
      if (ciudadMusicalSection) ciudadMusicalSection.style.display = "none";
      if (headingMusica) headingMusica.style.display = "none";
    } else if (hash === "#musica-folclorica") {
      if (historiaSection) historiaSection.style.display = "none";
      if (ciudadMusicalSection) ciudadMusicalSection.style.display = "block";
      if (headingMusica) headingMusica.style.display = "none";
    }
  }
}

